# Consultar el tipo de dato
# type(): Devuelve el tipo de dato almacenado.
print("Viendo tipos de datos:")
print(type(3)) # Tipo 'int' 
print(type(3.5)) # Tipo 'float'
print(type(1 + 2j)) # Tipo 'complex'
print(type("Hola Python")) # Tipo 'str' - string
print(type(True)) # Tipo 'bool' 
print(type([1, 2, 3])) # Tipo 'list'
print(type((1, 2, 3))) # Tipo 'tuple'
print(type({'name': 'Asabeneh'})) # Tipo 'diccionary'
print(type({9.8, 3.14, 5.66})) # Tipo 'set'
print(type(print("Mi cadena de texto"))) # Tipo 'NoneType'

