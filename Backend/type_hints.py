### Type Hints ###

my_string_var = 'My String Var'
print(my_string_var)
print(type(my_string_var))

my_string_var = 5
print(my_string_var)
print(type(my_string_var))

my_typed_var: int = 'My Typed String Var' # tipado deb
print(my_typed_var)
print(type(my_typed_var))

my_typed_var = 5
print(my_typed_var)
print(type(my_typed_var))
